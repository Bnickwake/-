t0 = np.array([300, 350, 400, 450 ,500]) # –ì—Ä–∞–¥—É—Å –¶–µ–ª—å—Å–∏—è
pk = 5 * kPa


for P0 in p0:
    KPD = []
    for T0 in t0:
        kpd_value = kpd(P0, T0, pk)
        KPD.append(kpd_value)
    plt.xlabel("t0, –≥—Ä–∞–¥—É—Å—ã –¶–µ–ª—å—Å–∏—è")
    plt.ylabel("–¢–µ—Ä–º–∏—á–µ—Å–∫–∏–π –ö–ü–î, %")
